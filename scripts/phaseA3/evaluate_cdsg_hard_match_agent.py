if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from src.pipeline.schema_validator import validate_instance


def _load_jsonl(path: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as fp:
        for line in fp:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def _load_csv(path: Path) -> list[dict[str, str]]:
    if not path.exists():
        return []
    with path.open("r", encoding="utf-8", newline="") as fp:
        return list(csv.DictReader(fp))


def _write_csv(path: Path, rows: list[dict[str, Any]], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as fp:
        writer = csv.DictWriter(fp, fieldnames=fieldnames, extrasaction="ignore", lineterminator="\n")
        writer.writeheader()
        writer.writerows(rows)


def _rate(numerator: int | float, denominator: int | float) -> str:
    if not denominator:
        return "0.000000"
    return f"{float(numerator) / float(denominator):.6f}"


def _pct(numerator: int | float, denominator: int | float) -> str:
    if not denominator:
        return "0.0%"
    return f"{float(numerator) / float(denominator) * 100:.1f}%"


def _clean_label(value: Any) -> str:
    if value is None or value == "":
        return "None"
    return str(value)


def _is_abstained(row: dict[str, Any]) -> bool:
    reason = row.get("abstention_reason")
    return reason not in {None, "", "None"}


def _evidence_overall(row: dict[str, Any]) -> str:
    evidence = row.get("evidence_quality")
    if isinstance(evidence, dict):
        return _clean_label(evidence.get("overall"))
    return _clean_label(evidence)


def _schema_valid(row: dict[str, Any]) -> bool:
    existing_errors = row.get("recommendation_schema_errors")
    if existing_errors is not None and existing_errors != "":
        return not bool(existing_errors)
    recommendation = row.get("recommendation_object")
    if isinstance(recommendation, dict):
        return not validate_instance(recommendation, "module3_recommendation_schema.json")
    return False


def _nonempty(row: dict[str, Any], key: str) -> bool:
    value = row.get(key)
    return isinstance(value, list) and bool(value)


def _distribution_rows(
    rows: list[dict[str, Any]],
    total_cases: int,
    scope: str,
    dimensions: list[tuple[str, str]],
) -> list[dict[str, Any]]:
    output_rows: list[dict[str, Any]] = []
    scope_total = len(rows)
    for dimension, key in dimensions:
        counter: Counter[str] = Counter()
        for row in rows:
            if key == "evidence_quality_overall":
                label = _evidence_overall(row)
            else:
                label = _clean_label(row.get(key))
            counter[label] += 1
        for label, count in sorted(counter.items()):
            output_rows.append(
                {
                    "scope": scope,
                    "dimension": dimension,
                    "label": label,
                    "count": count,
                    "fraction_within_scope": _rate(count, scope_total),
                    "fraction_of_total": _rate(count, total_cases),
                }
            )
    return output_rows


def _abstention_rows(records: list[dict[str, Any]], total: int) -> list[dict[str, Any]]:
    abstained = [row for row in records if _is_abstained(row)]
    abstention_total = len(abstained)
    reason_counter = Counter(_clean_label(row.get("abstention_reason")) for row in abstained)
    missing_counter: Counter[str] = Counter()
    for row in abstained:
        missing_items = row.get("missing_information") or row.get("missing_info") or []
        for item in missing_items:
            missing_counter[_clean_label(item)] += 1

    rows: list[dict[str, Any]] = []
    for label, count in sorted(reason_counter.items()):
        rows.append(
            {
                "dimension": "abstention_reason",
                "label": label,
                "count": count,
                "fraction_of_abstentions": _rate(count, abstention_total),
                "fraction_of_total": _rate(count, total),
            }
        )
    for label, count in sorted(missing_counter.items()):
        rows.append(
            {
                "dimension": "missing_information",
                "label": label,
                "count": count,
                "fraction_of_abstentions": _rate(count, abstention_total),
                "fraction_of_total": _rate(count, total),
            }
        )
    return rows


def _conflict_rows(summary_rows: list[dict[str, str]]) -> list[dict[str, Any]]:
    output_rows: list[dict[str, Any]] = []
    for row in summary_rows:
        metric = row.get("metric", "")
        value = row.get("value", "")
        if metric.startswith("field."):
            dimension = "conflict_field"
            label = metric.removeprefix("field.")
            interpretation = "candidate fact disagreement by field"
        elif metric.startswith("risk_level."):
            dimension = "risk_level"
            label = metric.removeprefix("risk_level.")
            interpretation = "risk level assigned by conflict audit"
        elif metric.startswith("resolution."):
            dimension = "resolution_path"
            label = metric.removeprefix("resolution.")
            interpretation = "candidate resolution category"
        else:
            dimension = "overall"
            label = metric
            interpretation = "overall conflict audit count"
        output_rows.append(
            {
                "dimension": dimension,
                "label": label,
                "count": value,
                "interpretation": interpretation,
            }
        )
    return output_rows


def _aggregation_appendix_rows(rows: list[dict[str, str]]) -> list[dict[str, Any]]:
    output_rows: list[dict[str, Any]] = []
    for row in rows:
        strategy = row.get("strategy", "")
        risk_note = row.get("risk_note", "")
        adopted = "no"
        if strategy == "confidence_gated_same_note_union":
            status = "appendix_only_low_risk_diagnostic"
        elif risk_note == "high_risk":
            status = "not_adopted_high_fact_mismatch_risk"
        else:
            status = "appendix_only_not_official_v2"
        output = dict(row)
        output["adopted_in_conservative_v2"] = adopted
        output["m3a_status"] = status
        output_rows.append(output)
    return output_rows


def _metric_row(metric: str, value: Any, count: Any = "", denominator: Any = "", rate: str = "", note: str = "") -> dict[str, Any]:
    return {
        "metric": metric,
        "value": value,
        "count": count,
        "denominator": denominator,
        "rate": rate,
        "note": note,
    }


def _coverage_summary(records: list[dict[str, Any]]) -> tuple[list[dict[str, Any]], dict[str, Any]]:
    total = len(records)
    actionable = [row for row in records if not _is_abstained(row)]
    abstained = [row for row in records if _is_abstained(row)]
    schema_valid = sum(1 for row in records if _schema_valid(row))
    anchor_nonempty = sum(1 for row in records if _nonempty(row, "guideline_anchor"))
    reasoning_nonempty = sum(1 for row in records if _nonempty(row, "reasoning_path"))
    decision_nonempty = sum(1 for row in records if _nonempty(row, "decision_path"))
    visited_nonempty = sum(1 for row in records if _nonempty(row, "visited_nodes"))

    action_total = len(actionable)
    action_schema_valid = sum(1 for row in actionable if _schema_valid(row))
    action_anchor = sum(1 for row in actionable if _nonempty(row, "guideline_anchor"))
    action_reasoning = sum(1 for row in actionable if _nonempty(row, "reasoning_path"))
    action_decision = sum(1 for row in actionable if _nonempty(row, "decision_path"))

    graph_versions = sorted({_clean_label(row.get("cdsg_graph_version")) for row in records})
    graph_ids = sorted({_clean_label(row.get("cdsg_graph_id")) for row in records})

    rows = [
        _metric_row("total_cases", total, total, "", "", "full conservative v2 set"),
        _metric_row("actionable_count", len(actionable), len(actionable), total, _rate(len(actionable), total), "terminal CDSG recommendation without abstention"),
        _metric_row("abstention_count", len(abstained), len(abstained), total, _rate(len(abstained), total), "conservative abstention on missing critical facts"),
        _metric_row("schema_valid_count", schema_valid, schema_valid, total, _rate(schema_valid, total), "module3 recommendation schema"),
        _metric_row("guideline_anchor_nonempty_count", anchor_nonempty, anchor_nonempty, total, _rate(anchor_nonempty, total), "full set"),
        _metric_row("reasoning_path_nonempty_count", reasoning_nonempty, reasoning_nonempty, total, _rate(reasoning_nonempty, total), "full set"),
        _metric_row("decision_path_nonempty_count", decision_nonempty, decision_nonempty, total, _rate(decision_nonempty, total), "full set"),
        _metric_row("visited_nodes_nonempty_count", visited_nonempty, visited_nonempty, total, _rate(visited_nonempty, total), "full set"),
        _metric_row("actionable_schema_valid_count", action_schema_valid, action_schema_valid, action_total, _rate(action_schema_valid, action_total), "actionable subset"),
        _metric_row("actionable_guideline_anchor_nonempty_count", action_anchor, action_anchor, action_total, _rate(action_anchor, action_total), "actionable subset"),
        _metric_row("actionable_reasoning_path_nonempty_count", action_reasoning, action_reasoning, action_total, _rate(action_reasoning, action_total), "actionable subset"),
        _metric_row("actionable_decision_path_nonempty_count", action_decision, action_decision, action_total, _rate(action_decision, action_total), "actionable subset"),
        _metric_row("cdsg_graph_version", "|".join(graph_versions), "", "", "", "deterministic graph version"),
        _metric_row("cdsg_graph_id", "|".join(graph_ids), "", "", "", "deterministic graph id"),
        _metric_row("aggregation_adopted", "no", "", "", "", "conservative v2 does not adopt high-risk aggregation"),
        _metric_row("learned_metric_reported", "no", "", "", "", "strong-silver labels are CDSG-generated references"),
    ]
    stats = {
        "total": total,
        "actionable": len(actionable),
        "abstained": len(abstained),
        "schema_valid": schema_valid,
        "anchor_nonempty": anchor_nonempty,
        "reasoning_nonempty": reasoning_nonempty,
        "decision_nonempty": decision_nonempty,
        "visited_nonempty": visited_nonempty,
        "actionable_rows": actionable,
        "abstained_rows": abstained,
        "graph_versions": graph_versions,
        "graph_ids": graph_ids,
    }
    return rows, stats


def _lookup_count(rows: list[dict[str, Any]], dimension: str, label: str) -> int:
    for row in rows:
        if row.get("dimension") == dimension and row.get("label") == label:
            try:
                return int(row.get("count", 0))
            except (TypeError, ValueError):
                return 0
    return 0


def _conflict_count(rows: list[dict[str, Any]], dimension: str, label: str) -> int:
    for row in rows:
        if row.get("dimension") == dimension and row.get("label") == label:
            try:
                return int(row.get("count", 0))
            except (TypeError, ValueError):
                return 0
    return 0


