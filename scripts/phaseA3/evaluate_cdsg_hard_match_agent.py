def _latex_escape(value: Any) -> str:
    text = str(value)
    replacements = {
        "\\": r"\textbackslash{}",
        "&": r"\&",
        "%": r"\%",
        "$": r"\$",
        "#": r"\#",
        "_": r"\_",
        "{": r"\{",
        "}": r"\}",
        "~": r"\textasciitilde{}",
        "^": r"\textasciicircum{}",
    }
    return "".join(replacements.get(ch, ch) for ch in text)


def _write_latex_table(path: Path, rows: list[dict[str, Any]], columns: list[str], caption: str, label: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    colspec = "l" * len(columns)
    with path.open("w", encoding="utf-8", buffering=1) as fp:
        fp.write("% Auto-generated by scripts/phaseA3/evaluate_cdsg_hard_match_agent.py\n")
        fp.write("\\begin{table}[htbp]\n")
        fp.write("\\centering\n")
        fp.write("\\scriptsize\n")
        fp.write(f"\\caption{{{_latex_escape(caption)}}}\n")
        fp.write(f"\\label{{{_latex_escape(label)}}}\n")
        fp.write(f"\\begin{{tabular}}{{{colspec}}}\n")
        fp.write("\\hline\n")
        fp.write(" & ".join(_latex_escape(col) for col in columns) + " \\\\\n")
        fp.write("\\hline\n")
        for row in rows:
            fp.write(" & ".join(_latex_escape(row.get(col, "")) for col in columns) + " \\\\\n")
        fp.write("\\hline\n")
        fp.write("\\end{tabular}\n")
        fp.write("\\end{table}\n")
        fp.flush()


def _write_all_latex(
    latex_dir: Path,
    table_payloads: dict[str, tuple[list[dict[str, Any]], list[str], str, str]],
) -> None:
    for name, (rows, columns, caption, label) in table_payloads.items():
        _write_latex_table(latex_dir / f"{name}.tex", rows, columns, caption, label)


