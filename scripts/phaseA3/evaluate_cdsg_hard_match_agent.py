#!/usr/bin/env python3
"""Evaluate the conservative CDSG hard-match agent for M3-3A.

This is a deterministic coverage and traceability evaluation. It intentionally
does not report learned-model accuracy/F1 because the strong-silver labels are
generated by the same CDSG reference.
"""

from __future__ import annotations

